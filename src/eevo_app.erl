-module(eevo_app).
-author("borja").
-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, StartArgs) ->
    Tables = eevo:attributes_table(),
    nndb:create_tables(Tables),
    nndb:start(Tables),
    eevo_sup:start_link(StartArgs).

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

